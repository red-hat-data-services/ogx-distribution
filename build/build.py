# Allowed characters for version strings: alphanumeric, dots, hyphens, plus, underscores
# Covers semver tags (v0.7.1+rhaiv.1), branch names (main, release-0.5.x), and short SHAs
_VERSION_PATTERN = re.compile(r"^[0-9a-zA-Z._+\-/]+$")

OGX_GIT_REPO = "https://github.com/opendatahub-io/ogx.git"

PINNED_DEPENDENCIES = ["milvus-lite>=3.0.0", "pymilvus!=2.6.10"]

CONSTRAINTS_FILE = Path("distribution/constraints.txt")

_SECRET_FIELD_WORDS = {"password", "secret", "token", "credential"}
_SECRET_FIELD_SUBSTRINGS = {"api_key", "access_key"}
_SECRET_FIELD_EXCLUDE_SUFFIXES = ("_file", "_path", "_url", "_dir")


STRIPPED_PROVIDER_TYPES = {
    "inline::sentence-transformers",
    "inline::milvus",
    "inline::faiss",
    "inline::docling",
    "inline::markitdown",
    "inline::pypdf",
    "remote::docling-serve",
}

STRIPPED_CONFIG_HEADER = (
    "# This file is automatically generated from build.yaml\n"
    "# by build/build.py - do not edit manually.\n"
    "#\n"
    "# Stripped provider types (dependency only - available via custom config.yaml):\n"
)


class BuildConfig(BaseSettings):
    model_config = SettingsConfigDict(
        env_file=Path(__file__).parent / "build.env",
    )

    ogx_version: str
    ogx_install_from_source: bool = False
    rhai_index_url: str | None = None

    @field_validator("ogx_version")
    @classmethod
    def check_version(cls, v: str) -> str:
        if not v or not _VERSION_PATTERN.match(v):
            raise ValueError(
                f"Invalid version format: {v!r}. "
                "Only alphanumeric characters, dots, hyphens, plus signs, "
                "underscores, and slashes are allowed."
            )
        return v


def _resolve_ref_to_sha(repo_url: str, ref: str) -> str:
    """Resolve a git ref (tag or branch) to a commit SHA via git ls-remote.

    For annotated tags, returns the dereferenced commit SHA.
    """
    result = subprocess.run(
        ["git", "ls-remote", "--tags", "--heads", repo_url, ref],
        capture_output=True,
        text=True,
        check=True,
        timeout=30,
    )

    sha = None
    for line in result.stdout.strip().splitlines():
        line_sha, line_ref = line.split("\t", 1)
        if line_ref.endswith("^{}"):
            return line_sha
        sha = line_sha

    if sha is None:
        raise ValueError(f"Could not resolve ref {ref!r} in {repo_url}")

    return sha


class OgxRequirements(NamedTuple):
    ogx_api: str
    ogx: str


class LockfileType(enum.StrEnum):
    MIDSTREAM = "midstream"
    DOWNSTREAM = "downstream"


class IndexConfig(NamedTuple):
    index_url: str
    torch_backend: str | None = None


class LockfileConfig(NamedTuple):
    output_path: Path
    index_config: IndexConfig


def _get_ogx_requirements(version: str, install_from_source: bool) -> OgxRequirements:
    """Resolve ogx package specifiers.

    When installing from source, the git tag is resolved to an immutable
    commit SHA via git ls-remote.
    """
    if install_from_source:
        sha = _resolve_ref_to_sha(OGX_GIT_REPO, version)
        return OgxRequirements(
            ogx_api=f"ogx-api @ git+{OGX_GIT_REPO}@{sha}#subdirectory=src/ogx_api",
            ogx=f"ogx @ git+{OGX_GIT_REPO}@{sha}",
        )
    else:
        return OgxRequirements(
            ogx_api=f"ogx-api=={version.split('+')[0]}",
            ogx=f"ogx=={version}",
        )


def _run(cmd: list[str], **kwargs) -> subprocess.CompletedProcess[str]:
    """Run a command, printing stdout/stderr on failure."""
    try:
        return subprocess.run(cmd, check=True, capture_output=True, text=True, **kwargs)
    except subprocess.CalledProcessError as e:
        print(f"Error running: {shlex.join(cmd)}")
        if e.stdout:
            print(f"stdout: {e.stdout}")
        if e.stderr:
            print(f"stderr: {e.stderr}")
        raise


def assert_command_installed(command):
    """Check if a command is installed and accessible."""
    if not shutil.which(command):
        print(f"Error: {command} not found. Please install it.")
        sys.exit(1)


@contextmanager
def _build_venv(
    packages: list[str],
    index_config: IndexConfig,
    constraints: Path | None = None,
) -> Generator[Path]:
    """Create a temporary venv with the given packages installed. Yields the venv path."""
    with tempfile.TemporaryDirectory() as tmpdir:
        venv_path = Path(tmpdir) / "venv"

        subprocess.run(
            ["uv", "venv", "--python", sys.executable, str(venv_path)],
            check=True,
            capture_output=True,
            text=True,
        )

        cmd = [
            "uv",
            "pip",
            "install",
            "--python",
            str(venv_path / "bin" / "python"),
            "--config-file",
            "/dev/null",
            "--default-index",
            index_config.index_url,
            *packages,
        ]
        if constraints:
            cmd.extend(["--constraint", str(constraints)])
        if index_config.torch_backend:
            cmd.extend(["--torch-backend", index_config.torch_backend])

        _run(cmd)
        yield venv_path


def generate_stripped_config():
    """Generate a stripped config.yaml from build.yaml, removing dependency-only providers."""
    build_path = Path("build/build.yaml")
    output_path = Path("distribution/config.yaml")

    if not build_path.exists():
        print(f"Error: {build_path} not found")
        sys.exit(1)

    ryaml = YAML()
    ryaml.preserve_quotes = True
    ryaml.width = 4096
    ryaml.indent(sequence=4, offset=2)

    with open(build_path, "r") as f:
        config = ryaml.load(f)

    # Validate that every entry in STRIPPED_PROVIDER_TYPES exists in build.yaml
    all_provider_types = {
        p.get("provider_type")
        for provider_list in config.get("providers", {}).values()
        if isinstance(provider_list, list)
        for p in provider_list
        if isinstance(p, dict)
    }
    missing = STRIPPED_PROVIDER_TYPES - all_provider_types
    if missing:
        print(
            f"Error: STRIPPED_PROVIDER_TYPES not found in build.yaml: {sorted(missing)}"
        )
        sys.exit(1)

    providers = config.get("providers", {})
    for api in list(providers.keys()):
        provider_list = providers[api]
        if isinstance(provider_list, list):
            for i in range(len(provider_list) - 1, -1, -1):
                p = provider_list[i]
                if (
                    isinstance(p, dict)
                    and p.get("provider_type") in STRIPPED_PROVIDER_TYPES
                ):
                    del provider_list[i]

    header = STRIPPED_CONFIG_HEADER + "".join(
        f"#   - {t}\n" for t in sorted(STRIPPED_PROVIDER_TYPES)
    )

    with open(output_path, "w") as f:
        f.write(header)
        ryaml.dump(config, f)

    print(f"Successfully generated {output_path}")


def _resolve_env_defaults(text):
    """Replace ${env.VAR:=default} and ${env.VAR:+value} templates.

    Pydantic validates build.yaml before env-var substitution, so
    non-string fields (e.g. integers) must contain plain values.
    Empty defaults are quoted to avoid YAML null interpretation.
    """

    def _replace_default(match):
        default = match.group(1)
        return f'"{default}"' if default == "" else default

    text = re.sub(r"\$\{env\.[^:}]+:=([^}]*)\}", _replace_default, text)
    text = re.sub(r"\$\{env\.[^:}]+:\+([^}]*)\}", r'"\1"', text)
    return text


def get_dependencies(ogx_bin: Path) -> list[str]:
    """Execute the ogx list-deps command and return a list of package specifiers."""
    build_yaml = Path("build/build.yaml")
    resolved = _resolve_env_defaults(build_yaml.read_text())
    with tempfile.NamedTemporaryFile(mode="w", suffix=".yaml", delete=False) as tmp:
        tmp.write(resolved)
        tmp_path = tmp.name
    try:
        result = _run([str(ogx_bin), "stack", "list-deps", tmp_path])
    finally:
        os.unlink(tmp_path)

    packages = []
    for line in result.stdout.splitlines():
        parts = iter(shlex.split(line))
        for part in parts:
            match part:
                # stripped because base images either already add the torch index as an extra index url, or include torch builds in the default index
                case "--extra-index-url" | "--index-url":
                    next(parts, None)
                # stripped because base images already provide the correct torch build, so sentence-transformers can safely resolve its dependencies without this guard
                case "--no-deps":
                    continue
                case _:
                    packages.append(part)

    return packages


def get_opentelemetry_packages(bootstrap_bin: Path) -> list[str]:
    """Run opentelemetry-bootstrap to discover instrumentation packages."""
    result = subprocess.run(
        [str(bootstrap_bin), "-a", "requirements"],
        capture_output=True,
        text=True,
        check=True,
    )
    packages = []
    for line in result.stdout.splitlines():
        line = line.strip()
        if line and not any(
            pkg in line
            for pkg in (
                "opentelemetry-instrumentation-botocore",
                "opentelemetry-instrumentation-exceptions",
                "opentelemetry-instrumentation-system-metrics",
            )
        ):
            packages.append(line)
    return packages


def _compile_lockfile(
    requirements_path: Path,
    output_path: Path,
    index_config: IndexConfig,
) -> None:
    """Run uv pip compile to produce a pinned lock file with hashes."""
    cmd = [
        "uv",
        "pip",
        "compile",
        "--constraint",
        str(Path("distribution/constraints.txt")),
        "--python-platform",
        "linux",
        "--python-version",
        "3.12",
        "--generate-hashes",
        "--config-file",
        "/dev/null",
        "--default-index",
        index_config.index_url,
        "--emit-index-url",
        str(requirements_path),
        "-o",
        str(output_path),
    ]
    if index_config.torch_backend:
        cmd.extend(["--torch-backend", index_config.torch_backend])

    output_path.unlink(missing_ok=True)
    _run(cmd)

    print(f"Successfully generated {output_path}")


def _build_requirements(
    dependencies: list[str],
    ogx_reqs: OgxRequirements,
    otel_packages: list[str],
) -> list[str]:
    """Combine all package specifiers into a sorted requirements list."""
    all_packages = sorted(set(dependencies + otel_packages))
    return [*ogx_reqs, *all_packages]


def _write_temp_requirements(lines: list[str]) -> Path:
    """Write package lines to a temporary requirements file. Caller must clean up."""
    path = Path(tempfile.gettempdir()) / "requirements.txt"
    path.write_text("\n".join(lines) + "\n")
    return path


def _get_lockfile_targets(
    install_from_source: bool, rhai_index_url: str | None
) -> dict[LockfileType, LockfileConfig]:
    """Determine which lock files to generate based on build configuration."""
    targets = {}
    if install_from_source:
        targets[LockfileType.MIDSTREAM] = LockfileConfig(
            Path("distribution/requirements-lock.txt"),
            IndexConfig(
                index_url="https://pypi.org/simple",
                torch_backend="cpu",
            ),
        )
    if rhai_index_url:
        targets[LockfileType.DOWNSTREAM] = LockfileConfig(
            Path("distribution/requirements-lock-konflux.txt"),
            IndexConfig(index_url=rhai_index_url),
        )
    if not targets:
        print(
            "Error: OGX_INSTALL_FROM_SOURCE=false and RHAI_INDEX_URL is not set. "
            "At least one lock file target is required."
        )
        sys.exit(1)
    return targets


def generate_config_labels(version: str) -> str:
    """Generate OCI LABEL instruction with base64-encoded config.yaml."""
    config_path = Path("distribution/config.yaml")
    if not config_path.exists():
        print(f"Error: {config_path} not found")
        sys.exit(1)

    encoded = base64.b64encode(config_path.read_bytes()).decode("ascii")

    labels = [
        ("com.ogx.config.config.yaml", encoded),
        ("com.ogx.distribution.name", "rh"),
        ("com.ogx.distribution.version", version),
        ("com.ogx.distribution.default-config", "config.yaml"),
        ("com.ogx.distribution.configs", "config.yaml"),
        ("org.opencontainers.image.title", "OGX - rh"),
        ("org.opencontainers.image.version", version),
    ]

    first = f'LABEL {labels[0][0]}="{labels[0][1]}" \\'
    rest = [f'  {k}="{v}"' for k, v in labels[1:]]
    return first + "\n" + " \\\n".join(rest)


def generate_containerfile(version: str):
    """Generate Containerfile from template with OCI config labels."""
    template_path = Path("Containerfile.in")
    output_path = Path("Containerfile")

    if not template_path.exists():
        print(f"Error: Template file {template_path} not found")
        sys.exit(1)

    template_content = template_path.read_text()
    placeholder_count = template_content.count("{config_labels}")
    if placeholder_count != 1:
        print(
            f"Error: Containerfile.in must contain exactly one '{{config_labels}}' placeholder, found {placeholder_count}"
        )
        sys.exit(1)

    warning = (
        "# WARNING: This file is auto-generated from Containerfile.in\n"
        "# by build/build.py - do not edit manually.\n"
    )

    # Use str.replace() to avoid format string injection from label content
    containerfile_content = warning + template_content
    containerfile_content = containerfile_content.replace(
        "{config_labels}", generate_config_labels(version)
    )

    output_path.write_text(containerfile_content)
    print(f"Successfully generated {output_path}")


def _is_secret_field(field_name: str) -> bool:
    """Heuristic: does this YAML config field name hold a secret value?"""
    lower = field_name.lower()
    if lower.endswith(_SECRET_FIELD_EXCLUDE_SUFFIXES):
        return False
    words = set(lower.split("_"))
    if words & _SECRET_FIELD_WORDS:
        return True
    return any(sub in lower for sub in _SECRET_FIELD_SUBSTRINGS)


def _extract_secret_env_vars_from_yaml(yaml_path: Path) -> set[str]:
    """Walk build.yaml and return env var names referenced by secret fields."""
    env_ref = re.compile(r"\$\{env\.([^:}]+):[=+]")
    secrets: set[str] = set()

    def _walk(node, parent_key=""):
        if isinstance(node, dict):
            for key, value in node.items():
                _walk(value, parent_key=key)
        elif isinstance(node, list):
            for item in node:
                _walk(item, parent_key=parent_key)
        elif isinstance(node, str):
            if _is_secret_field(parent_key):
                for match in env_ref.finditer(node):
                    secrets.add(match.group(1))

    with open(yaml_path) as f:
        from yaml import safe_load

        data = safe_load(f)
    _walk(data)
    return secrets


def _extract_entrypoint_secrets(entrypoint_path: Path) -> set[str]:
    """Extract the secret var names from the entrypoint.sh for-loop."""
    text = entrypoint_path.read_text()
    match = re.search(r"for _secret_var in\s*\\(.*?);\s*do", text, re.DOTALL)
    if not match:
        print(f"Error: could not find _secret_var loop in {entrypoint_path}")
        sys.exit(1)
    body = match.group(1).replace("\\", " ")
    return {v for v in body.split() if v}


def verify_file_secrets_sync():
    """Verify that entrypoint.sh _FILE list matches secrets in build.yaml."""
    yaml_path = Path("build/build.yaml")
    entrypoint_path = Path("distribution/entrypoint.sh")

    yaml_secrets = _extract_secret_env_vars_from_yaml(yaml_path)
    entrypoint_secrets = _extract_entrypoint_secrets(entrypoint_path)

    missing_from_entrypoint = yaml_secrets - entrypoint_secrets
    extra_in_entrypoint = entrypoint_secrets - yaml_secrets

    if missing_from_entrypoint or extra_in_entrypoint:
        print(
            "Error: distribution/entrypoint.sh _FILE secret list is out of sync "
            "with build/build.yaml."
        )
        if missing_from_entrypoint:
            print(f"  Add to entrypoint.sh:    {sorted(missing_from_entrypoint)}")
        if extra_in_entrypoint:
            print(f"  Remove from entrypoint.sh: {sorted(extra_in_entrypoint)}")
        print(
            "\nWhen adding a new provider with secret fields (api_key, password, "
            "token, etc.) to build/build.yaml, also add the env var to the "
            "_FILE resolution loop in distribution/entrypoint.sh."
        )
        sys.exit(1)

    print(
        f"Verified {len(yaml_secrets)} secret env vars have _FILE support in entrypoint.sh"
    )


def main():
    config = BuildConfig()

    ogx_reqs = _get_ogx_requirements(config.ogx_version, config.ogx_install_from_source)

    assert_command_installed("uv")

    print("Generating stripped config.yaml...")
    generate_stripped_config()

    targets = _get_lockfile_targets(
        config.ogx_install_from_source, config.rhai_index_url
    )

    for name, target in targets.items():
        print(f"Generating {target.output_path}...")

        print("  Getting dependencies...")
        with _build_venv([*ogx_reqs], target.index_config) as venv:
            dependencies = get_dependencies(venv / "bin" / "ogx")

        # Temporary: RHAI index doesn't have all markitdown[all] extras deps
        if name == LockfileType.DOWNSTREAM:
            dependencies = [
                re.sub(r"^markitdown\[.*\]", "markitdown", d) for d in dependencies
            ]

        print("  Discovering opentelemetry instrumentation packages...")
        with _build_venv(
            [*ogx_reqs] + dependencies,
            target.index_config,
            constraints=Path("distribution/constraints.txt"),
        ) as venv:
            otel_packages = get_opentelemetry_packages(
                venv / "bin" / "opentelemetry-bootstrap"
            )

        requirements = _build_requirements(dependencies, ogx_reqs, otel_packages)

        print("  Compiling lock file...")
        tmp_path = _write_temp_requirements(requirements)
        try:
            _compile_lockfile(tmp_path, target.output_path, target.index_config)
        finally:
            os.unlink(tmp_path)

    print("Generating Containerfile...")
    generate_containerfile(config.ogx_version)

    print("Verifying _FILE secret sync...")
    verify_file_secrets_sync()

    print("Done!")


if __name__ == "__main__":
    main()
