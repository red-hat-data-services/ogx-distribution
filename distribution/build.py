# Allowed characters for version strings: alphanumeric, dots, hyphens, plus, underscores
# Covers semver tags (v0.7.1+rhaiv.1), branch names (main, release-0.5.x), and short SHAs
_VERSION_PATTERN = re.compile(r"^[0-9a-zA-Z._+\-/]+$")


def _validate_version(version: str) -> str:
    """Validate a version string contains only safe characters.

    Raises ValueError if the version contains shell metacharacters or
    other unexpected characters that could lead to injection.
    """
    if not version or not _VERSION_PATTERN.match(version):
        raise ValueError(
            f"Invalid version format: {version!r}. "
            "Only alphanumeric characters, dots, hyphens, plus signs, underscores, and slashes are allowed."
        )
    return version


STRIPPED_PROVIDER_TYPES = {
    "inline::sentence-transformers",
    "inline::milvus",
    "inline::faiss",
}

STRIPPED_CONFIG_HEADER = (
    "# This file is automatically generated from build.yaml\n"
    "# by distribution/build.py - do not edit manually.\n"
    "#\n"
    "# Stripped provider types (dependency only - available via custom config.yaml):\n"
)


def _load_versions() -> dict[str, str]:
    """Load version strings from distribution/versions.env."""
    versions = {}
    versions_path = Path(__file__).parent / "versions.env"
    with open(versions_path) as f:
        for line in f:
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                key, value = line.split("=", 1)
                versions[key.strip()] = value.strip()
    return versions


def _init_versions() -> tuple[str, str]:
    """Resolve OGX_VERSION and OGX_CLIENT_VERSION from versions.env and environment."""
    versions = _load_versions()
    for val in versions.values():
        _validate_version(val)

    ogx_version: str
    if override := os.getenv("OGX_VERSION"):
        ogx_version = _validate_version(override)
    else:
        ogx_version = f"v{versions['OGX_VERSION']}+{versions['OGX_RHAI_VERSION']}"

    ogx_client_version: str
    if client := versions.get("OGX_CLIENT_VERSION"):
        ogx_client_version = client
    else:
        base_version = ogx_version.split("+")[0]
        ogx_client_version = ".".join(base_version.split(".")[:3])

    return ogx_version, ogx_client_version


OGX_VERSION, OGX_CLIENT_VERSION = _init_versions()
BASE_REQUIREMENTS = [
    f"ogx=={OGX_VERSION}",
]

